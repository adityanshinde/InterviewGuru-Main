function entry(id, category, question, variants, answer) {
  return { id, category, question, variants, answer };
}

function concept(difficulty, spoken, bullets, sections, extra = {}) {
  return { type: 'concept', difficulty, spoken, bullets, sections, ...extra };
}

function coding(difficulty, spoken, bullets, sections, code, codeLanguage = 'javascript') {
  return {
    type: 'coding',
    difficulty,
    spoken,
    bullets,
    sections,
    code,
    codeLanguage,
  };
}

function behavioral(difficulty, spoken, bullets, sections) {
  return { type: 'behavioral', difficulty, spoken, bullets, sections };
}

function systemDesign(difficulty, spoken, bullets, sections) {
  return { type: 'system_design', difficulty, spoken, bullets, sections };
}

function section(title, content, points) {
  return { title, content, points };
}

/** Expand a topic list into full entries */
function expandCategory(prefix, category, items, answerFn) {
  return items.map((item, i) => {
    const idx = String(i + 1).padStart(3, '0');
    const [question, ...variantRest] = Array.isArray(item) ? item : [item.q, ...(item.variants || [])];
    const variants = variantRest.length ? variantRest : [`Tell me about ${question.toLowerCase().replace(/\?$/, '')}`];
    return entry(`${prefix}_${idx}`, category, question, variants.slice(0, 3), answerFn(item, i));
  });
}

